s = "++++++++++++++++++++++++++++++++++++++++++++++++.++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.++.----------->@>>.<@<<<.@<@<@<++++<.<@<@<<@<-----.<<<<<.<@<@<+<.+>@.-------.-------->>>.<@<@<++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.++>>>.<@<@<<.-----------<.>@>@<@><<.>@>@++++<.>@-----.>>>.<@<@<+<.>@+.-------.--------.+++++++++++++>>>>>>.<@<@<@<@<@<<.>@++.-------<.>@+++++++<<<.>@>@>@<<.>@>@<.>@-<.>@++++++++++++<<<.>@>@>@+++++++++++€"

values = [0]

for a in s:
    if a == '+':
        values[-1] += 1
    elif a == '-':
        values[-1] -= 1
    elif a == '<':
        x = [values[-1]]
        x.extend(values[:-1])
        values = x
    elif a == '@':
        values[-1], values[-2] = values[-2], values[-1]
    elif a == '>':
        values.append(values[0])
        values = values[1:]
    elif a == '.':
        values.append(values[-1])
    else: # a == '€':
        print("CTFlearn{"+''.join(map(chr, values))+ "}")